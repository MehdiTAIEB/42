int		*ft_map(int *tab, int length, int (*f)(int))
{
	int i;
	int *t;

	i = 0;
	t = (int*)malloc(sizeof(*t) * (length));
	while (i < length)
	{
		t[i] = f(tab[i]);
		i++;
	}
	return (t);
}
