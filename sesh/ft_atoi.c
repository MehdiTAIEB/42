#include "cpp.h"

int		ft_atoi(char *str)
{
	int i;
	int nb;

	i = 1;
	nb = 0;
	while ((*str >= 9 && *str <= 13) || (*str == 32))
	{
		str++;
	}
	if (*str == 43)
	{
		str++;
	}
	if (*str == 45)
	{
		i *= -1;
		str++;
	}
	while (*str <= '9' && *str >= '0' && *str)
	{
		nb *= 10;
		nb += *str - '0';
		str++;
	}
	return (nb * i);
}
