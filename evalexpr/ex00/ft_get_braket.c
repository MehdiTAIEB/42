#include "cpp.h"

int 	ft_get_braket(char *expr)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while (expr[i] != '\0')
	{
		if (expr[i] == 40)
		{
			y = i;
		}
		i++;
	}
	return (y);
}
