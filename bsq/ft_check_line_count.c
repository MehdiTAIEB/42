#include "cpp.h"

int		ft_check_line_count(char *map)
{
	int i;
	int wc;
	char c;

	i = 0;
	wc = 0;
	while (map[i] != '\0')
	{
		if (map[i] == '\n')
			wc++;
		i++;
	}
	wc--;
	c = wc + '0';
	if (c != map[0])
		return (0);
	return (wc);
}
